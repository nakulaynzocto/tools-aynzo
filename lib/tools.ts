const categoryMap: Record<string, string> = {
    'image': 'Image Tools',
    'text': 'Text Tools',
    'developer': 'Code & Developer Tools',
    'seo': 'SEO & Web Tools',
    'youtube': 'YouTube Tools',
    'social': 'Social & Links',
    'security': 'Security & Crypto',
    'crypto': 'Security & Crypto',
    'converter': 'Converters',
    'utility': 'Utility Tools',
    'pdf': 'Converters',
    'calculator': 'Calculators'
};

// Tool Interface
export interface Tool {
    _id: string;
    name: string;
    slug: string;
    description: string;
    category: string;
    status: 'published' | 'draft';
    seo: {
        title: string;
        description: string;
    };
    hidden?: boolean;
    group?: string;
}

// Get all tools
export function getAllTools(): Tool[] {
    return tools as Tool[];
}

// Get tool by slug
export function getToolBySlug(slug: string): Tool | undefined {
    return tools.find(t => t.slug === slug) as Tool | undefined;
}

// Get tools organized by categories (Dynamic)
export const toolCategories: Record<string, { name: string; slug: string; group?: string }[]> = tools.reduce((acc, tool) => {
    // Hidden tools are now included in the menu (Navbar)
    const label = categoryMap[tool.category] || tool.category;
    if (!acc[label]) acc[label] = [];
    acc[label].push({ name: tool.name, slug: tool.slug, group: tool.group });
    return acc;
}, {} as Record<string, { name: string; slug: string; group?: string }[]>);

// Search tools
export function searchTools(query: string) {
    const q = query.toLowerCase();
    return tools.filter(t => t.name.toLowerCase().includes(q) || t.slug.toLowerCase().includes(q));
}

// Legacy API compatibility wrapper
export const api = {
    getProduct: async (slug: string) => {
        const tool = getToolBySlug(slug);
        return tool ? { success: true, data: tool } : { success: false, message: 'Tool not found' };
    },
    getProducts: async () => {
        return { success: true, data: { products: tools } };
    }
};
